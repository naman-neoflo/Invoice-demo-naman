/**
 * An individual observation Neoflo captured. The "raw event" — many of
 * these may eventually crystallise into a drafted rule, but most just
 * stay in the ledger as historical reasoning.
 *
 * When the observation is a *decision* (an override, a tone choice, an
 * exception approval), Neoflo also captures the human-supplied
 * rationale. That rationale is the raw material that rules synthesise
 * from — it's the bridge from individual judgment to institutional
 * memory.
 */
export interface Observation {
  id: string
  /** ISO timestamp (already-formatted) — demo seed */
  occurredAt: string
  /** Who took the action */
  actor: string
  /** Where Neoflo observed it */
  source: ObservationSource
  kind: ObservationKind
  workflow: Workflow
  /** Plain-English summary of what happened */
  headline: string
  /** Longer description with the data Neoflo captured */
  detail: string
  /** Personas this observation matters to */
  relevantPersonas: PersonaId[]
  /**
   * Human-supplied "why" for decision-class observations (overrides,
   * exception approvals, deliberate tone shifts). When present, this is
   * what distinguishes a judgment from a routine action and what gets
   * distilled into rule reasoning.
   */
  decisionRationale?: string
  /** The cognitive lift — what pattern this might create (1 line) */
  patternHint?: string
  /** If a rule was drafted/accepted from this observation, the rule id */
  rulePromoted?: string
  /** Recurrence count — "we've seen this N times" */
  recurrenceCount?: number
}

// ─────────────────────────────────────────────────────────────────────────
// Rules — drafted, active, or refused
// ─────────────────────────────────────────────────────────────────────────

export type RuleStatus = "drafted" | "active" | "refused"

/**
 * Multi-step approval. For example a rule that changes GL coding logic
 * might need AP Manager → Controller; one that touches segregation of
 * duties might need AP Manager → Controller → CFO.
 *
 * `currentStep` is the next pending approver (0 = first). When the
 * chain is complete the rule's status flips drafted → active.
 */
export interface ApprovalChain {
  approvers: PersonaId[]
  /** Index of the next pending approver. equals approvers.length when complete. */
  currentStep: number
  /** Per-step audit — who approved when, in chain order. */
  history?: Array<{ approver: PersonaId; at: string }>
}

/**
 * The visible synthesis Neoflo did when promoting observations into a
 * rule. Surfaced on every drafted rule so users see the working —
 * conditions, exceptions, and what would *not* trigger the rule. Same
 * spirit as showing an LLM's chain of thought, but structured.
 */
export interface SynthesisTrace {
  /** Observation ids the synthesis pulled from. */
  contributingObservationIds: string[]
  /** Concrete conditions the rule checks (machine-readable phrasing). */
  conditions: string[]
  /** Exception cases that opt out of the rule. */
  exceptions: string[]
  /** What would NOT trigger the rule — the rejection criteria. */
  rejectionCriteria: string[]
}

export interface Rule {
  id: string
  status: RuleStatus
  /** The synthesised rule, in plain English */
  statement: string
  /** Why Neoflo drafted it */
  reasoning: string
  /** Workflow it applies to */
  workflow: Workflow
  /** Multi-step approval chain — first approver is the owner, last is the activator. */
  approvalChain: ApprovalChain
  /** Observations that drove this rule */
  observationCount: number
  /** Visible synthesis trace — empty for refused rules that never got that far. */
  synthesis?: SynthesisTrace
  /** When the rule changed status (drafted, accepted, refused) */
  statusChangedAt: string
  /** For active rules: how many times applied in the last 30d */
  appliedCount?: number
  /** Optional impact callout */
  impact?: { value: string; label: string }
}

/** Convenience: the first persona in the approval chain is the rule owner. */
export function getRuleOwner(rule: Rule): PersonaId {
  return rule.approvalChain.approvers[0]
}

/**
 * A rule is "ready for SOP" when it's been stable enough that promoting
 * it into the policy doc makes sense. Criteria (demo heuristic):
 *   - status = active
 *   - applied ≥ 15 times in the last 30d (signals it's running reliably)
 *
 * In production we'd also require zero exception flags since promotion,
 * but the demo seed doesn't track per-rule exception events.
 */
export function isReadyForSOP(rule: Rule): boolean {
  return rule.status === "active" && (rule.appliedCount ?? 0) >= 15
}

/**
 * Cluster of observations that share a patternHint. The pattern is the
 * unit — actors and sources can vary. Used by the observation feed to
 * collapse density: 6 events sharing "Acme -B duplicate" naming
 * render as a single card rather than 6.
 */
export interface ObservationCluster {
  /** The patternHint string the cluster groups around. */
  patternHint: string
  /** Events in this cluster, sorted by recency (newest first). */
  events: Observation[]
  /** Unique actors across the cluster. */
  actors: string[]
  /** Unique sources across the cluster. */
  sources: ObservationSource[]
  /** The dominant workflow (most-frequent across events). */
  workflow: Workflow
  /** Oldest event timestamp in this cluster. */
  earliestAt: string
  /** Newest event timestamp in this cluster. */
  latestAt: string
  /** Rule id this pattern promoted into, if any (first non-null wins). */
  rulePromoted?: string
}

/**
 * Partition a list of observations into clusters + singletons.
 *
 * Rules (per design doc 2026-05-19-observation-clustering-design.md):
 * - Observations sharing the same patternHint group together
 * - Cross-actor, cross-source, cross-time clustering is allowed
 * - Observations with no patternHint, or whose group has only 1 event,
 *   are returned as singletons
 * - Within a cluster, events are sorted newest-first
 * - Clusters sorted by latest event recency; singletons by own
 *   timestamp recency
 */
export function clusterObservations(observations: Observation[]): {
  clusters: ObservationCluster[]
  singletons: Observation[]
} {
  const byPattern = new Map<string, Observation[]>()
  const noPattern: Observation[] = []
  for (const o of observations) {
    if (o.patternHint && o.patternHint.trim().length > 0) {
      const list = byPattern.get(o.patternHint) ?? []
      list.push(o)
      byPattern.set(o.patternHint, list)
    } else {
      noPattern.push(o)
    }
  }

  const clusters: ObservationCluster[] = []
  const singletons: Observation[] = [...noPattern]

  for (const [patternHint, events] of byPattern.entries()) {
    if (events.length < 2) {
      // Singleton cluster → returned in the singletons list per design
      singletons.push(events[0])
      continue
    }
    // Sort events newest-first within the cluster
    events.sort((a, b) =>
      a.occurredAt > b.occurredAt ? -1 : a.occurredAt < b.occurredAt ? 1 : 0,
    )
    const actors = Array.from(new Set(events.map((e) => e.actor)))
    const sources = Array.from(new Set(events.map((e) => e.source)))
    // Workflow = mode (most frequent). For ties, take the first event's.
    const workflowCounts = new Map<Workflow, number>()
    for (const e of events) {
      workflowCounts.set(e.workflow, (workflowCounts.get(e.workflow) ?? 0) + 1)
    }
    let workflow: Workflow = events[0].workflow
    let maxCount = 0
    for (const [w, c] of workflowCounts.entries()) {
      if (c > maxCount) {
        workflow = w
        maxCount = c
      }
    }
    const rulePromoted = events.find((e) => e.rulePromoted)?.rulePromoted
    clusters.push({
      patternHint,
      events,
      actors,
      sources,
      workflow,
      earliestAt: events[events.length - 1].occurredAt,
      latestAt: events[0].occurredAt,
      rulePromoted,
    })
  }

  // Sort clusters by recency of latest event (newest first)
  clusters.sort((a, b) =>
    a.latestAt > b.latestAt ? -1 : a.latestAt < b.latestAt ? 1 : 0,
  )
  // Sort singletons by recency
  singletons.sort((a, b) =>
    a.occurredAt > b.occurredAt ? -1 : a.occurredAt < b.occurredAt ? 1 : 0,
  )

  return { clusters, singletons }
}

