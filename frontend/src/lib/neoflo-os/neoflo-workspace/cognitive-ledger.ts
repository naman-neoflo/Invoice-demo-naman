// lib/neoflo-workspace/cognitive-ledger.ts
//
// The Cognitive Ledger — Neoflo's operational reasoning layer.
// While the GL records what happened financially, the Cognitive Ledger
// records WHY decisions were made + HOW exceptions were resolved + WHICH
// patterns recur. Neoflo captures these signals by:
//
//   1. Observing the user's actions inside the Neoflo tool itself
//      (classifier re-labels, override approvals, dunning tone choices,
//      GL re-codes, vendor-master tweaks).
//
//   2. Observing the user's actions outside the tool via the Neoflo
//      desktop agent — screen captures of NetSuite vendor card lookups,
//      SAP FBL3N pulls, Slack escalations, Excel exports, Coupa PO
//      changes, etc.
//
// Over time, recurring observations get synthesized into RULES that the
// finance organization can adopt — turning ephemeral judgment into
// persistent institutional memory.
//
// Data here is demo seed only. In production each observation would be
// generated by the agent runtime + a pattern-detection model.

