export function filterObservationsByPersona(
  observations: Observation[],
  persona: PersonaId,
): Observation[] {
  if (persona === "all") return observations
  return observations.filter(
    (o) =>
      o.relevantPersonas.includes("all") ||
      o.relevantPersonas.includes(persona),
  )
}

/**
 * A persona sees a rule if they're anywhere in its approval chain —
 * not just the owner. That way a Controller sees rules the AP Manager
 * proposed that need their sign-off next.
 */
export function filterRulesByPersona(
  rules: Rule[],
  persona: PersonaId,
): Rule[] {
  if (persona === "all") return rules
  return rules.filter((r) => r.approvalChain.approvers.includes(persona))
}

export function getRule(id: string): Rule | undefined {
  return SEED_RULES.find((r) => r.id === id)
}

export function getObservation(id: string): Observation | undefined {
  return SEED_OBSERVATIONS.find((o) => o.id === id)
}

export function ledgerStats() {
  return {
    observationsTotal: SEED_OBSERVATIONS.length,
    activeRules: SEED_RULES.filter((r) => r.status === "active").length,
    draftedRules: SEED_RULES.filter((r) => r.status === "drafted").length,
    refusedRules: SEED_RULES.filter((r) => r.status === "refused").length,
    sourcesObserved: Array.from(
      new Set(SEED_OBSERVATIONS.map((o) => o.source)),
    ).length,
  }
}
